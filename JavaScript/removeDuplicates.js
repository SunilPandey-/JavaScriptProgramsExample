function removeDuplicates(num1)
  {
    var num =JSON.parse("["+num1+"]");
    var x,
      len=num.length,
      out=[],
      obj={};
 
  for (x=0; x<len; x++) {
    obj[num[x]]=0;
  }
  for (x in obj) {
    out.push(x);
  }
  return out;


  }


